// String methods example
let str = "JavaScript is awesome";

// Length
console.log("Length of string:", str.length);

// Searching
console.log("Index of 'is':", str.indexOf("is"));

// Extracting
console.log("Substring from index 11:", str.substring(11));

// Replacing
let newStr = str.replace("awesome", "amazing");
console.log("Replaced string:", newStr);

// Concatenation
let anotherStr = " with GPT-3.5";
console.log("Concatenated string:", str.concat(anotherStr));
